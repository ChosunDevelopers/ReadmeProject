package com.readme.dto;

public class BoardLikeVO {

	private String id;
	private String title;
	private String comment;
	private String teamBoard_bno;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getTeamBoard_bno() {
		return teamBoard_bno;
	}
	public void setTeamBoard_bno(String teamBoard_bno) {
		this.teamBoard_bno = teamBoard_bno;
	}
	
	
}
